    AFTER_AVERAGE_DEPTH      NUMBER(10,4),
    AFTER_AVERAGE_OVERLAPS   NUMBER(10,4),
    DEPTH_IMPROVEMENT_PCT    NUMBER(8,2),
    OVERLAP_IMPROVEMENT_PCT  NUMBER(8,2),
    STATUS                   VARCHAR(20) NOT NULL,
    ERROR_MESSAGE            VARCHAR(4096),
    EXECUTED_AT              TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

-- 5. Synthetic data load (5M rows)
-- Uses a generator to create realistic sportsbook betting data
INSERT INTO BET_TRANSACTIONS (
    BET_ID, CUSTOMER_ID, EVENT_ID, SPORT, LEAGUE, BET_TYPE,
    STAKE_AMOUNT, ODDS, POTENTIAL_PAYOUT, BET_STATUS, PLACED_AT, SETTLED_AT
)
SELECT
    SEQ4() AS BET_ID,
    UNIFORM(1, 100000, RANDOM()) AS CUSTOMER_ID,
    UNIFORM(1, 5000, RANDOM()) AS EVENT_ID,
    CASE UNIFORM(1, 6, RANDOM())
        WHEN 1 THEN 'NFL'
        WHEN 2 THEN 'NBA'
        WHEN 3 THEN 'MLB'
        WHEN 4 THEN 'NHL'
        WHEN 5 THEN 'SOCCER'
        ELSE 'MMA'
    END AS SPORT,
    CASE UNIFORM(1, 6, RANDOM())
        WHEN 1 THEN 'National Football League'
        WHEN 2 THEN 'National Basketball Association'
        WHEN 3 THEN 'Major League Baseball'
        WHEN 4 THEN 'National Hockey League'
        WHEN 5 THEN 'English Premier League'
        ELSE 'Ultimate Fighting Championship'
    END AS LEAGUE,
    CASE UNIFORM(1, 5, RANDOM())
        WHEN 1 THEN 'MONEYLINE'
        WHEN 2 THEN 'SPREAD'
        WHEN 3 THEN 'OVER_UNDER'
        WHEN 4 THEN 'PARLAY'
        ELSE 'PROP'
    END AS BET_TYPE,
    ROUND(UNIFORM(5, 5000, RANDOM())::NUMBER(12,2) / 100, 2) AS STAKE_AMOUNT,
    ROUND(UNIFORM(100, 2000, RANDOM())::NUMBER(8,4) / 100, 4) AS ODDS,
    ROUND(STAKE_AMOUNT * ODDS, 2) AS POTENTIAL_PAYOUT,
    CASE UNIFORM(1, 4, RANDOM())
        WHEN 1 THEN 'WON'
        WHEN 2 THEN 'LOST'
        WHEN 3 THEN 'PENDING'
        ELSE 'VOID'
    END AS BET_STATUS,
    DATEADD('second',
        -UNIFORM(0, 86400 * 365, RANDOM()),
        CURRENT_TIMESTAMP()
    ) AS PLACED_AT,
    CASE
        WHEN BET_STATUS IN ('WON', 'LOST', 'VOID')
        THEN DATEADD('hour', UNIFORM(1, 72, RANDOM()), PLACED_AT)
        ELSE NULL
    END AS SETTLED_AT
FROM TABLE(GENERATOR(ROWCOUNT => 5000000));

