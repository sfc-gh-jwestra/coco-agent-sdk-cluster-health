-- 2. BET_TRANSACTIONS table with clustering key
CREATE TABLE IF NOT EXISTS BET_TRANSACTIONS (
    BET_ID          NUMBER(38,0) NOT NULL,
    CUSTOMER_ID     NUMBER(38,0) NOT NULL,
    EVENT_ID        NUMBER(38,0) NOT NULL,
    SPORT           VARCHAR(50) NOT NULL,
    LEAGUE          VARCHAR(100),
    BET_TYPE        VARCHAR(30) NOT NULL,
    STAKE_AMOUNT    NUMBER(12,2) NOT NULL,
    ODDS            NUMBER(8,4) NOT NULL,
    POTENTIAL_PAYOUT NUMBER(14,2),
    BET_STATUS      VARCHAR(20) DEFAULT 'PENDING',
    PLACED_AT       TIMESTAMP_NTZ NOT NULL,
    SETTLED_AT      TIMESTAMP_NTZ,
    CREATED_AT      TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
)
CLUSTER BY (SPORT, PLACED_AT::DATE);

-- 3. NOTIFICATION_RECIPIENTS table
